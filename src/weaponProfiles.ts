function generateFallback(recoil: number, _range: number, type: string): WeaponProfile {
  const r = recoil / 72;
  const tpp = Math.round(148 * (0.85 + r * 0.15));
  const fpp = Math.round(tpp * 0.84);
  const red = Math.round(58 / (0.8 + r * 0.3));
  const s2 = Math.round(red * 0.72);
  const s3 = Math.round(red * 0.52);
  const s4 = Math.round(red * 0.40);
  const s6 = Math.round(red * 0.24);
  const s8 = Math.round(red * 0.16);
  const tm = type === "Sniper" ? 0.85 : type === "DMR" ? 0.90 : type === "SMG" ? 1.08 : type === "Shotgun" ? 1.12 : type === "LMG" ? 0.96 : 1.0;
  const a = (v: number) => Math.round(v * tm);
  return P(
    [a(tpp), a(fpp), a(red), a(s2), a(s3), a(s4), a(s6), a(s8)],
    [a(Math.round(tpp*0.89)), a(Math.round(fpp*0.89)), a(Math.round(red*0.88)), a(Math.round(s2*0.92)), a(Math.round(s3*0.92)), a(Math.round(s4*0.92)), a(Math.round(s6*0.88)), a(Math.round(s8*0.88))],
    [a(Math.round(tpp*1.88)), a(Math.round(fpp*2.02)), a(Math.round(red*4.86)), a(Math.round(s2*6.0)), a(Math.round(s3*7.4)), a(Math.round(s4*7.7)), a(Math.round(s6*7.0)), a(Math.round(s8*6.1))],
    [a(Math.round(tpp*1.82)), a(Math.round(fpp*1.96)), a(Math.round(red*4.93)), a(Math.round(s2*6.2)), a(Math.round(s3*7.5)), a(Math.round(s4*8.0)), a(Math.round(s6*7.5)), a(Math.round(s8*6.7))],
    recoil * 0.7, recoil * 0.4, 80, 600,
  );
}

